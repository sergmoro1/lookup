<h4>Значения свойств</h4>
<p>
	Свойства имеют значения. Необходимо выбрать свойство, ввести название значения и код, связанный с названием. 
</p>
<p>
	Например свойство "Цвет", значение "Синий".
</p>
<p>
	Свойства могут быть связаны с любыми моделями (сущностями). Например товар может иметь "Цвет", "Фасон" и прочее. 
</p>
